package com.mistake.day5.demo6;

public class Factor {
    private int limit;
    private int times;
    private String address;

    public Factor(int limit, int times, String address) {
        this.limit = limit;
        this.times = times;
        this.address = address;
    }

    public int getLimit() {
        return limit;
    }

    public int getTimes() {
        return times;
    }

    public String getAddress() {
        return address;
    }
}

